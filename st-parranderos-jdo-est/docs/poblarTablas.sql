--RFC1 TOTAL RECOLECTADO POR HABITACION

SELECT ID_HABITACION, SUM(PRECIO) AS TOTAL_RECOLECTADO
FROM(
SELECT ID_HABITACION, PRECIO
FROM(
(SELECT * FROM ISIS2304C021910.FACTURAS
WHERE ID_SERVICIO IS NOT NULL AND FECHA > TO_DATE('2019-01-01', 'YYYY-MM-DD'))A
INNER JOIN ISIS2304C021910.ESTADIAS
ON A.ID_ESTADIA = ISIS2304C021910.ESTADIAS.ID_ESTADIA))
GROUP BY ID_HABITACION;

-- RFC2 TOTAL RECOLECTADO POR SERVICIO (USAR PARAMETROS)

SELECT A.ID_SERVICIO, SUM(PRECIO) AS TOTAL_RECOLECTADO
FROM 
((SELECT * FROM ISIS2304C021910.FACTURAS
WHERE ID_SERVICIO IS NOT NULL AND FECHA > TO_DATE('2019-01-01', 'YYYY-MM-DD') AND FECHA < TO_DATE('2019-12-31', 'YYYY-MM-DD'))A
INNER JOIN ISIS2304C021910.SERVICIOS
ON A.ID_SERVICIO = ISIS2304C021910.SERVICIOS.ID_SERVICIO)
GROUP BY A.ID_SERVICIO
ORDER BY SUM(PRECIO)
FETCH FIRST 20 ROWS ONLY;

-- RFC3 

SELECT ID_HABITACION, (NUM_PERSONAS / CAPACIDAD)
FROM (ISIS2304C021910.ESTADIAS A
